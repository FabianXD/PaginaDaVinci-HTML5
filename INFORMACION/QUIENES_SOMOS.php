            <b> LEONARDO DA VINCI SCHOOL </b> es un establecimiento educacional mixto, diurno, particular subvencionado, con jornada escolar completa, cuya modalidad de enseñanza es científica - humanista, con énfasis en el idioma Inglés. Imparte educación Pre-básica, Básica y Media, centrada en el respeto a la persona. Está ubicado en la avenida Francisco Bilbao 1720 de la ciudad de Pitrufquén, Novena Región de la Araucanía. <br><br>

Los alumnos están distribuidos en un curso por nivel, desde Pre-Kinder a IV medio, con un máximo de 30 alumnos por curso.<br><br>

Para desarrollar el proceso educativo de los alumnos, el colegio cuenta para complementar su trabajo académico, con un gimnasio techado, sala de computación, biblioteca, una sala multiuso con medios audiovisuales diversos y un amplio entorno natural que lo rodea.<br><br>

Los alumnos del colegio desarrollan un alto nivel de autoexigencia académica, una personalidad empática, optimista, reflexiva, alegre y autónoma, el espíritu crítico, incorporan e internalizan valores como la solidaridad, la honestidad, la responsabilidad, el respeto, el espíritu democrático y la tolerancia, que los distingue.<br><br>

El cuerpo docente, que en su mayoría forman parte de la planta exclusiva del colegio, está integrado por profesores con experiencia académica y vocación de servicio, comprometidos con el proyecto educativo, exigentes en la formación de sus alumnos y autoexigentes y críticos en su quehacer profesional. Son además profesionales en su quehacer y con permanente interés por perfeccionarse y actualizarse. Todo esto redunda en un mejor nivel educacional a nuestros alumnos, reflejado en los resultados de las pruebas SIMCE y PSU, en la breve trayectoria institucional, ubicándolo en el mejor de la comuna y sobre los promedios regionales y nacionales. Ello también se debe al ambiente familiar que se vive al interior del establecimiento, lo que permite un mejor conocimiento de los niños y jóvenes, de sus actitudes, necesidades, conflictos e intereses, entre otros.<br><br>

Los padres y apoderados, conscientes de su rol de ser los primeros educadores de sus hijos, son personas comprometidas con el proceso de formación de sus hijos. Se caracterizan por ser colaboradores y respetuosos de las normas del colegio, de los roles y competencias de cada uno de los estamentos de la comunidad y de sus miembros.<br><br>

NUESTRA FILOSOFÍA<br>

En el plano de la confesionalidad nuestro establecimiento educacional no discrimina a alumnos, apoderados, y/o funcionarios, por su pensamiento religioso y en sus reglamentos hace omisión de los dogmas de dichos credos. Asimismo en su quehacer docente no realiza proselitismo religioso alguno, a excepción de la clase de religión que es optativa y, en su convivencia diaria propenderá al respeto de todos los credos religiosos, pues entendemos que dichas inclinaciones religiosas son responsabilidad en primer lugar de la familia, en segundo lugar de las respectivas confesiones religiosas, con las cuales se pueden tener estrechas relaciones de colaboración en circunstancias que lo ameritan.<br><br>

Aspiramos a tener un currículum que considere las necesidades propias de la naturaleza humana; como ser biológico, afectivo y social, y, como metas educativas se propone entregarle al estudiante las competencias que le permitan aprender a PENSAR, SENTIR , ACTUAR, COMPARTIR y QUERER o PERSISTIR en sus propósitos, atendiendo así las dimensiones cognitivas, afectivas, efectivas, sociales y volitivas.<br><br>

También aspiramos a desarrollar el trabajo académico en forma innovadora, activa, implicativa, creativa, y significativa. Innovadora, por cuanto busca formas nuevas de mediar entre la acción del profesor y el aprendizaje del alumno; Significativa, por cuanto facilita la construcción del conocimiento por parte de los alumnos; Implicativa, activa y creativa, con el principal propósito de atraer y comprometer al estudiante con su aprendizaje.<br><br>

El colegio pretende obtener de sus alumnos altos niveles de aprendizajes, dado lo cual, exige "el compromiso de los alumnos y la participación responsable de la familia.<br><br>

El colegio propone entregar una adecuada orientación personal, educacional y vocacional, que llevará al estudiante a conocerse a sí mismo e integrarse adecuadamente a la vida familiar, social y laboral.<br><br>
