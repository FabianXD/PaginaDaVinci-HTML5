            <div class="nocolor">
P: Qué debo hacer si mi hijo/a falta al colegio o yo como apoderado a las reuniones y/o citaciones? <br>
R: Toda inasistencia debe ser justificada personalmente por el apoderado titular o suplente dentro de las 48 horas de la ausencia.
</div>
<br>
<div class="sicolor">
P:¿Qué debo hacer si mi hijo/a tiene que retirarse antes de la jornada de clases? <br>
R : Si un alumno debe retirarse durante la jornada de clases, sólo podrá hacerlo si su apoderado titular o suplente viene personalmente a buscarlo firmando el libro correspondiente. <br>
En caso de que un alumno por problemas de salud inesperado deba retirarse, se le comunicará al apoderado telefónicamente de la situación para que concurra al establecimiento a retirarlo y en caso de alguna dificultad se enviará a su casa con un adulto responsable, previa autorización del apoderado.
</div>
<br>
<div class="nocolor">
P:¿Qué hacer cuando tengo un problema con mi hijo en el colegio? <br>
R: Ante cualquier problema que tenga su hijo en el colegio, que requiera ser conversado entre Profesor y Apoderado, se debe seguir el CONDUCTO REGULAR establecido para esto. La línea establecida es primero con el Profesor, luego el Profesor Jefe y finalmente la Dirección del Colegio.
</div>
<br>
<div class="sicolor">
P: ¿Cómo puedo comunicarme con un profesor por alguna inquietud relacionada con el desempeño académico y/o disciplinario de mi hijo/a? <br>
R: a)Todos los profesores tienen un horario de atención individual a apoderados, fijado desde el inicio del año escolar e informado. <br>
b) Los apoderados serán citados por el profesor mediante una comunicación escrita enviada con el alumno, telefónicamente por secretaría o inspectoría o venir libremente en los horarios establecidos. <br>
c) Los apoderados no deberán venir en horario de clases del profesor ni al inicio ni término de las jornadas. <br>
d) Las atenciones de apoderados deben ser informadas por escrito a Dirección en formulario existente para ello, informando el motivo de la citación o entrevista y la firma del apoderado.
</div>
<br>
<div class="nocolor">
P: ¿Qué hacer en caso de que a mi hijo/a le ocurra algún accidente mientras esté en el colegio? <br>
R: En caso de accidente de un alumno, el colegio procede de la siguiente manera: <br>
a) se le presta los primeros auxilios. <br>
b) se da cuenta a Inspectoría o Dirección de la situación. <br>
c) se hace la declaración de accidente y en forma paralela se le avisa al apoderado de lo acontecido telefónicamente. <br>
d) Se envía al accidentado al hospital en un taxi con una persona adulta responsable. <br>
e) el apoderado deberá concurrir al centro asistencial para hacerse cargo de su hijo.<br>
</div>
<br>
<div class="sicolor">
P: ¿Dónde puede almorzar mi hijo/a en el colegio si vive fuera de la ciudad? <br>
R: Pese a que el colegio no cuenta con casino ni cocina, existe un espacio físico y microondas para que el alumno caliente su colación traída de la casa si así lo prefiere. Es recomendable usar termos para comida con el fin de optimizar el tiempo.
</div>
<br>
<div class="nocolor">
P: ¿A quién acudo en caso de que mi hijo/a pierda prendas del uniforme o sus útiles escolares? <br>
R: Consultar en el colegio si se hubiese quedado en la sala o el patio. El alumno debe hacerse responsable de todas sus pertenencias. Debe tener todo marcado con su nombre y el colegio no se hace responsable de accesorios como teléfonos, pendrives,  computadores personales, etc.
</div>
<br>
<div class="sicolor">
P: ¿Debe mi hijo/a asistir a clases de extraescolar? <br>
R : Sí, las clases son obligatorias para todos los alumnos desde 3° básico a 4° año medio, con un porcentaje mínimo de asistencia del 85% para ser promovido de curso.
</div>
<br>
<div class="nocolor">
P: ¿Qué son las salidas a terreno? <br>
R: Se consideran salidas a terreno aquellos proyectos interdisciplinarios que incluyen salidas desde una hora de clases hasta tres días, incluyendo Sábado y Domingo. <br>
Para las salidas a terreno se requiere que: <br>
a) El profesor responsable de la salida informe a dirección del proyecto a realizar.<br> 
b) Solicite autorización escrita al apoderado, comunicando con la debida anticipación la salida e informando acerca del lugar a visitar, objetivo, actividad a realizar, hora de inicio y término, puntos de partida y retorno, medio de movilización, adultos responsables. <br>
c) Entregue en Inspectoría la nómina de alumnos participantes, adjuntando las autorizaciones de los padres. <br>
d) Registre la salida en el libro de Novedades. <br>
e) Ningún alumno podrá salir del Colegio sin la autorización escrita del apoderado. <br>
f) No está permitido en ninguna salida a terreno, cualquiera que esta sea, el consumo de alcohol, ni para estudiantes ni profesor. <br>
g) El profesor responsable de la salida coordinará el medio de transporte a utilizar. <br>
h) En caso de que la salida a terreno contemple más de un día, el profesor es el responsable ante la dirección del establecimiento de cumplir con la normativa que exige el Ministerio de Transportes para estos efectos.
</div>
<br>
<div class="sicolor">
P:¿ Qué cursos están autorizados para usar un polerón especial? ¿Qué condiciones pone el colegio? <br>
R:a) Sólo los alumnos de cuarto año medio <br>
b) Deberán ser diseñados por los mismos alumnos en conjunto con su profesor jefe en cuanto a color y leyenda, y la dirección del establecimiento sancionará o aprobará finalmente el diseño elegido.<br> 
c) El uso del polerón está permitido durante todo el año escolar, siempre que no sea una ocasión formal.
</div>
<br>
<div class="nocolor">
P: ¿Hasta qué curso los alumnos/as deben usar delantal /cotona obligatoriamente? <br>
R: El uso del delantal y/o cotona es obligatorio desde Pre kínder hasta Cuarto Año Básico.
</div>
<br>
<div class="sicolor">
P: ¿El colegio cuenta con movilización propia o convenio con alguna empresa de transportes? <br>
R: No, el colegio no cuenta con movilización propia para el traslado de los alumnos, ni tiene convenio con ninguna empresa de transporte escolar.
</div>
<br>
<div class="nocolor">
P:¿Por cuánto tiempo el colegio se hace responsable de los alumnos antes del inicio y después del término de la jornada escolar? <br>
R: El colegio se hará responsable de los alumnos durante el horario de clases, a excepción de los alumnos de Pre-Kinder y Kínder que será hasta 15 minutos después de la hora de salida de su jornada.
</div>
<br>
<div class="sicolor">
P: ¿Con qué frecuencia se hacen las reuniones de apoderados? ¿Son obligatorias? <br>
R: Las reuniones de apoderados se realizan una vez al mes y son calendarizadas por el establecimiento al inicio del año escolar, normalmente la tercera semana del mes. El día y la hora lo convienen entre apoderados y profesor/a. <br>
Si, son obligatorias. El apoderado debe asistir a ellas al menos en un 75%.
