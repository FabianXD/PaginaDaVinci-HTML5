            Personificó el ideal renacentista por su empeño en conocer y dominar todas las artes y técnicas. Incursionó en la pintura, escultura, música, poesía, ingeniería, arquitectura, física, matemáticas, botánica, anatomía y filosofía. <br>
            <br>
            <div>
            <img class="imgbio" src="IMG/leonardo.jpg" >
            <p class="titulo_noticias3">DATOS BIOGRÁFICOS</p>
Nació en Vinci, cerca de la ciudad italiana de Florencia, el 15 de abril de 1452.<br>
<br>
A los 15 años ingresó al taller del escultor y pintor Andrea del Verrocchio, en Florencia, donde estuvo por casi diez años. Su primer trabajo fue el rostro de un ángel en el cuadro "El bautismo de Cristo".De esta época son la "Anunciación " y "La adoración de los Magos", entre otros. También realizó gran cantidad de bocetos y bosquejos.<br>
<br>
</div>
En 1482 se fue a Milán, donde se inició como pintor. Durante ese tiempo pintó el "Retrato de Cecilia Gallerani", "La dama del armiño", una versión de "La Virgen de las rocas" y "La última cena".<br>
<div>
<br>
<img class="imgbio" src="IMG/ultima_cena.jpg" >
En 1499, Leonardo volvió a Florencia. En 1502, durante diez meses viajó por distintos lugares, como arquitecto militar e ingeniero. Dibujó planos de ciudades y mapas topográficos y supervisó las obras en las fortalezas de los territorios papales del centro de Italia. De este período es uno de sus cuadros más famosos, "La Gioconda".<br>
</div>
<br>
En 1506 regresó a Milán y de esta época son la segunda versión de la "Virgen de las Rocas" y "La Virgen, el Niño Jesús y Santa Ana".<br>
<br>
En 1513 se fue a Roma, bajo el mecenazgo de Giuliano de Médicis. Vivía en el Vaticano, ocupado en experimentos científicos y técnicos. <br>
En 1516 se fue a Francia, llamado por el rey Francisco I, donde siguió trabajando como pintor, arquitecto e ingeniero. Allí falleció el 2 de mayo de 1519, a los 67 años.<br>
<p class="titulo_noticias3">SUS DIVERSOS APORTES</p>
Cultivó tanto las artes como la ciencia. Escribió estudios de pintura, arquitectura, mecánica, anatomía -gracias a la disección de cadáveres, dibujó con detalle y claridad gran parte de los órganos del cuerpo-, matemáticas, óptica, geología, botánica, física, hidráulica, la estática y la dinámica, la mecánica del vuelo, la navegación, etc.<br>
<br>
Proyectó máquinas de distintos tipos: textil, para propulsión, levantar pesos, molinos, martillos, bombas, aparatos de transmisión y armas. Adelantándose a su época, ideó una especie de tanque, un paracaídas, una máquina para volar y un barco subacuático.<br>
<p class="titulo_noticias3">NOVEDADES</p>
Leonardo innovó en la pintura al introducir el sfumato y el efecto del claroscuro . <br>
El sfumato sustituye el dibujo lineal por un modelado vaporoso que funde las formas de sus personajes con la atmósfera que los rodea.<br>
<br>
Además, redujo la intensidad de los colores, trabajando en el claroscuro o contraste de las luces y sombras.<br>
<br>
La primera obra en la que aparece el sfumato y el uso del claroscuro fue "La adoración de los Magos" (1481).<br>
<br>
El sfumato se considera totalmente logrado en la primera versión de "La Virgen de las Rocas" (1483-1486), donde también ocupó la composición triangular de las figuras humanas.<br>
